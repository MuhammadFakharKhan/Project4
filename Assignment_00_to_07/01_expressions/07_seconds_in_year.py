def main():


    print("=== Seconds in a Year Calculator ===")
    days_in_year = 365
    hours_in_day = 24
    minutes_in_an_hour = 60
    seconds_in_a_minute = 60
    seconds_in_a_year = days_in_year * hours_in_day * minutes_in_an_hour * seconds_in_a_minute
    print(f"There are {seconds_in_a_year} seconds in a year.")
    print("Seconds in a year calculation complete.")




if __name__ =='__main__':
    main()